// languages.valueOf() returns the same array as languages
const languages = ["C", "C++", "Java", "Python", "JavaScript"];
const myLanguages = languages.valueOf();
console.log(myLanguages);
// ['C', 'C++', 'Java', 'Python', 'JavaScript']

// numbers.valueOf() returns the same array as numbers
const numbers = [8, 3, 49, 25, 4, 8, 12];
const myNumbers = numbers.valueOf();
console.log(myNumbers);
// [8, 3, 49, 25, 4, 8, 12]
