// Example 1: Converting a string into an array of characters
const str = "SCALive";
console.log(Array.from(str));
// Output: ['S', 'C', 'A', 'L', 'i', 'v', 'e']

// Example 2: Converting a Set into an array
const set = new Set([1, 2, 3, 4, 5]);
console.log(Array.from(set));
// Output: [1, 2, 3, 4, 5]
